  def numSatisfies(grouping: Seq[Int], records: String): Long = {
    // println(s"$records, $grouping")
    val sumGrouping = grouping.sum
    val knownBroken = records.count(r => r == '#')
    val nonResolved = records.count(r => r == '?')
    if (sumGrouping == knownBroken + nonResolved) {
      val recordGroups = records.split('.').filter(_.nonEmpty).map(_.length)
      if (recordGroups.length == grouping.length && (0 until recordGroups.length).forall(i => recordGroups(i) == grouping(i))) {
        1L
      } else {
        0L
      }
    } else if (sumGrouping > knownBroken + nonResolved || knownBroken > sumGrouping) {
      0L
    } else if (knownBroken == sumGrouping) {
      // val recordGroups = records.replace('?', '.').split('.').filter(_.nonEmpty).map(_.length)
      // if (recordGroups.length == grouping.length && (0 until recordGroups.length).forall(i => recordGroups(i) == grouping(i))) {
      //   1L
      // } else {
      //   0L
      // }
      numSatisfies(grouping, records.replace('?', '.'))
    } else {
      assert(sumGrouping < knownBroken + nonResolved && knownBroken < sumGrouping)
      assert(nonResolved > 0)
      assert(sumGrouping > 0)
      val firstNonResolved = records.indexOf('?')
      val firstKnownBroken = records.indexOf('#')
      if (firstKnownBroken < 0 || firstNonResolved < firstKnownBroken) {
        numSatisfies(grouping, records.substring(firstNonResolved + 1)) +
        numSatisfies(grouping, "#" + records.substring(firstNonResolved + 1))
      } else {
        val startRecords = records.substring(firstKnownBroken)
        if ((0 until grouping.head).exists(i => startRecords(i) == '.') || (startRecords.length > grouping.head && startRecords(grouping.head) == '#')) {
          0L
        } else if (startRecords.length < grouping.head + 2) {
          1L
        } else {
          numSatisfies(grouping.tail, startRecords.substring(grouping.head + 1))
        }
      }
    }
    // 0L
  }
  
